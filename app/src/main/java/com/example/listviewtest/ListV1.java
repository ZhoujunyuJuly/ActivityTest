package com.example.listviewtest;

public class ListV1{
    private String left;
    private String right;

    public ListV1(){
        super();
    }

    public ListV1(String left,String right){
        super();
        this.left = left;
        this.right = right;
    }

    public String getLeft(){
        return left;
    }

    public void setLeft(){
        this.left = left;
    }

    public String getRight(){
        return right;
    }

    public void setRight(){
        this.right = right;
    }

}
